// BuyVistas - Main JavaScript
document.addEventListener('DOMContentLoaded', function() {
    initHeader();
    initMobileMenu();
    initSearch();
    updateCartCount();
});

// Header scroll behavior
function initHeader() {
    const header = document.querySelector('.header-sticky');
    if (!header) return;
    
    window.addEventListener('scroll', function() {
        if (window.scrollY > 10) {
            header.classList.add('scrolled');
        } else {
            header.classList.remove('scrolled');
        }
    });
}

// Mobile menu toggle
function initMobileMenu() {
    const menuBtn = document.querySelector('[data-mobile-menu-btn]');
    const mobileMenu = document.querySelector('[data-mobile-menu]');
    const menuIcon = document.querySelector('[data-menu-icon]');
    
    if (!menuBtn || !mobileMenu) return;
    
    menuBtn.addEventListener('click', function() {
        const isOpen = mobileMenu.classList.toggle('open');
        menuIcon.innerHTML = isOpen ? getSVGIcon('x') : getSVGIcon('menu');
    });
    
    // Close menu when clicking a link
    mobileMenu.querySelectorAll('a').forEach(link => {
        link.addEventListener('click', () => {
            mobileMenu.classList.remove('open');
            menuIcon.innerHTML = getSVGIcon('menu');
        });
    });
}

// Search functionality
function initSearch() {
    const searchBtn = document.querySelector('[data-search-btn]');
    const searchContainer = document.querySelector('[data-search-container]');
    const searchInput = document.querySelector('[data-search-input]');
    const searchClose = document.querySelector('[data-search-close]');
    
    if (!searchBtn || !searchContainer) return;
    
    searchBtn.addEventListener('click', function() {
        searchContainer.classList.add('open');
        searchInput?.focus();
    });
    
    searchClose?.addEventListener('click', function() {
        searchContainer.classList.remove('open');
    });
    
    // Handle search form submission
    const searchForm = document.querySelector('[data-search-form]');
    if (searchForm) {
        searchForm.addEventListener('submit', function(e) {
            e.preventDefault();
            const query = searchInput?.value.trim();
            if (query) {
                window.location.href = `shop.html?search=${encodeURIComponent(query)}`;
            }
        });
    }
}

// Render stars
function renderStars(rating) {
    let html = '<div class="stars">';
    for (let i = 1; i <= 5; i++) {
        if (i <= Math.floor(rating)) {
            html += getSVGIcon('star-filled');
        } else {
            html += getSVGIcon('star-empty');
        }
    }
    html += '</div>';
    return html;
}

// Create product card HTML
function createProductCard(product) {
    const discount = Math.round(((product.original_price - product.price) / product.original_price) * 100);
    
    return `
        <a href="product.html?id=${product.id}" class="product-card group" data-testid="product-card-${product.id}">
            <div class="product-card-image-container">
                <img src="${product.image}" alt="${product.name}" class="product-card-image" loading="lazy">
                
                <div class="product-badges">
                    ${product.is_bestseller ? '<span class="badge badge-gold">Bestseller</span>' : ''}
                    ${discount > 0 ? `<span class="badge">${discount}% Off</span>` : ''}
                </div>
                
                <button onclick="event.preventDefault(); event.stopPropagation(); handleQuickAdd('${product.id}')" 
                        class="quick-add-btn" data-testid="add-to-cart-${product.id}">
                    ${getSVGIcon('shopping-bag')}
                    Add to Cart
                </button>
            </div>
            
            <div class="product-card-info">
                <p class="caption-text">${product.category}</p>
                <h3 class="product-card-title">${product.name}</h3>
                ${renderStars(product.rating)}
                <span class="reviews-count">(${product.reviews_count})</span>
                <div class="product-card-price">
                    <span class="price-current">${formatPrice(product.price)}</span>
                    ${product.original_price > product.price ? `<span class="price-original">${formatPrice(product.original_price)}</span>` : ''}
                </div>
            </div>
        </a>
    `;
}

// Handle quick add to cart
function handleQuickAdd(productId) {
    addToCart(productId, 1);
    showToast('Added to cart!');
}

// Render product grid
function renderProductGrid(products, containerId) {
    const container = document.getElementById(containerId);
    if (!container) return;
    
    if (products.length === 0) {
        container.innerHTML = `
            <div class="no-products">
                <p>No products found</p>
            </div>
        `;
        return;
    }
    
    container.innerHTML = products.map(product => createProductCard(product)).join('');
}

// Get URL parameters
function getUrlParams() {
    const params = new URLSearchParams(window.location.search);
    return {
        category: params.get('category'),
        search: params.get('search'),
        sort: params.get('sort'),
        id: params.get('id')
    };
}

