// Newsletter subscription (display only)
function handleNewsletterSubmit(e) {
    e.preventDefault();
    const email = document.querySelector('[data-newsletter-email]')?.value;
    if (email) {
        const form = e.target;
        form.innerHTML = '<p class="text-gold">Thank you for subscribing!</p>';
    }
}
